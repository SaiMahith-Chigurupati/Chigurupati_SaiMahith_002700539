public class VitalSigns {
    private int systole;
    private int diastole;
    private int preDiet;
    private int postDiet;
    private int heartRate;
    private String comments;

    public int getSystole() {
        return systole;
    }

    public void setSystole(int systole) {
        this.systole = systole;
    }

    public int getDiastole() {
        return diastole;
    }

    public void setDiastole(int diastole) {
        this.diastole = diastole;
    }

    public int getPreDiet() {
        return preDiet;
    }

    public void setPreDiet(int preDiet) {
        this.preDiet = preDiet;
    }

    public int getPostDiet() {
        return postDiet;
    }

    public void setPostDiet(int postDiet) {
        this.postDiet = postDiet;
    }

    public String getComments() {
        return comments;
    }

    public void setComments(String comments) {
        this.comments = comments;
    }

    public int getHeartRate() {
        return heartRate;
    }

    public void setHeartRate(int heartRate) {
        this.heartRate = heartRate;
    }
    
    
    
}
