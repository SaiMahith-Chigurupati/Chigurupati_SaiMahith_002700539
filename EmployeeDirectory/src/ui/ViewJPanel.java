    private void populateTable() {
        
        // this function is used to populate the employee data in the database table
        
        DefaultTableModel model = (DefaultTableModel) tblEmployees.getModel();
        model.setRowCount(0);
        
        for(EmployeeDetails ed: data.getData()){
            
            Object[] Row =new Object[10];
            Row[0] = ed;
            Row[1] = ed.getEmpId();
            Row[2] = ed.getAge();
            Row[3] = ed.getGender();
            Row[4] = ed.getStartDate();
            Row[5] = ed.getLevel();
            Row[6] = ed.getTeamInfo();
            Row[7] = ed.getPositionTitle();
            Row[8] = ed.getMobile();
            Row[9] = ed.getEmail();
            
            model.addRow(Row);
        }
    }
    
    private void populateSearchResults(ArrayList<EmployeeDetails> ed){
        //this function is used to populate the items matching the search content
        
        DefaultTableModel model = (DefaultTableModel) tblEmployees.getModel();
        model.setRowCount(0);
        
        for(EmployeeDetails e: ed){
            
            Object[] Row =new Object[10];
            Row[0] = e;
            Row[1] = e.getEmpId();
            Row[2] = e.getAge();
            Row[3] = e.getGender();
            Row[4] = e.getStartDate();
            Row[5] = e.getLevel();
            Row[6] = e.getTeamInfo();
            Row[7] = e.getPositionTitle();
            Row[8] = e.getMobile();
            Row[9] = e.getEmail();
            
            model.addRow(Row);
        }
}
