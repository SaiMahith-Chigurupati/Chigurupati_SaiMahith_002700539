        JOptionPane.showMessageDialog(this, "Employee Details updated.");
        
        populateTable();
        
        txtName.setText("");
        txtEmpId.setText("");
        txtAge.setText("");
        txtGender.setText("");
        txtLevel.setText("");
        chStartDate.setDate(null);
        txtTeamInfo.setText("");
        txtPositionTitle.setText("");
        txtMobile.setText("");
        txtEmail.setText("");
        lblImage.setIcon(new ImageIcon());
        
        
