    private void btnBrowseActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBrowseActionPerformed
        // TODO add your handling code here:
        
        //whenever user clicks on browse button on create screen to upload an image using below code.
        
        browseImageFile.addChoosableFileFilter(fnef);
        
        int showImage = browseImageFile.showOpenDialog(null);
        
        if(showImage == JFileChooser.APPROVE_OPTION){
            File selectedImageFile =browseImageFile.getSelectedFile();
            String selectedImagePath =selectedImageFile.getAbsolutePath();
            lblPath.setText(selectedImagePath);
            
            ImageIcon ii = new ImageIcon(selectedImagePath);
            Image image =ii.getImage().getScaledInstance(100,150 , Image.SCALE_SMOOTH);
            
            lblImage.setIcon(new ImageIcon(image));
         
        }
    }//GEN-LAST:event_btnBrowseActionPerformed

    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:
        
        //whenever User clicks on Save button on create screen, EMployee details entered 
        //will undergo validations and data will be saved into the database(object) using this function
        
        String name=txtName.getText();
        String empId = txtEmpId.getText();
        int age =0;
        int flag =0;
        
        try{
            age = Integer.parseInt(txtAge.getText());
        }catch(Exception e){
            flag++;
            //JOptionPane.showMessageDialog(this, "Please enter Valid Age");

        }
        
        String gender = txtGender.getText();
        Date startDate = chStartDate.getDate();
        String level = txtLevel.getText();
        String teamInfo = txtTeamInfo.getText();
        String positionTitle = txtPositionTitle.getText();
        String mobile = txtMobile.getText();
        String email =txtEmail.getText();
        int temp=99;
        
        File selectedImageFile =browseImageFile.getSelectedFile();
        String selectedImagePath="";
        
        try{
        
        selectedImagePath =selectedImageFile.getAbsolutePath();
        }catch(Exception e){
             temp=0;
            //JOptionPane.showMessageDialog(this, "Please Upload a Photo");

        }
                
        
        if(name.isBlank() || teamInfo.isBlank() || level.isBlank()|| positionTitle.isBlank() || empId=="" || email.isBlank()){
            JOptionPane.showMessageDialog(this, "Please enter Mandatory Fields");

        }
        
        else if(flag!=0 || age<0 || age >80){
            JOptionPane.showMessageDialog(this, "Please enter a valid age");
        }
        
        else if(empId.contains("0123456789")){
            JOptionPane.showMessageDialog(this, "Please enter a valid Employee ID");

    }
        
        
        else if(mobile.length()!=10 || mobile.isBlank()==true || mobile.chars().allMatch( Character::isDigit )!=true){
            JOptionPane.showMessageDialog(this, "Please enter a valid Mobile Number");
            
            txtMobile.setText("");
            
            
        }
        
        else if((email.toLowerCase().endsWith(".com")!= true) || email.contains("@")!=true || email.startsWith("@")){
            JOptionPane.showMessageDialog(this, "Please enter a valid Email Adddress");
            
            txtEmail.setText("");
            
        }
        
        else if (temp==0){
            JOptionPane.showMessageDialog(this, "Please upload a Photo");
            
        }
        
        else {
        
            EmployeeDetails ed = data.addNewEmployee();

            
        ed.setName(name);
        ed.setEmpId(empId);
        ed.setAge(age);
        ed.setGender(gender);
        ed.setLevel(level);
        ed.setStartDate(startDate);
        ed.setTeamInfo(teamInfo);
        ed.setPositionTitle(positionTitle);
        ed.setMobile(mobile);
        ed.setEmail(email);
        ed.setSelectedImagePath(selectedImagePath);
        
 
        
        JOptionPane.showMessageDialog(this, "New Employee added.");
        
        txtName.setText("");
        txtEmpId.setText("");
        txtAge.setText("");
        txtGender.setText("");
        txtLevel.setText("");
        chStartDate.setDate(null);
        txtTeamInfo.setText("");
        txtPositionTitle.setText("");
        txtMobile.setText("");
        txtEmail.setText("");
        lblPath.setText(" ");
        lblImage.setIcon(new ImageIcon());
        
        }
        
    }//GEN-LAST:event_btnSaveActionPerformed


